    public static class Colour {

        public final int rgb24;

        public final int red;
        public final int green;
        public final int blue;

        public final float red_float;
        public final float green_float;
        public final float blue_float;

        private Colour(int rgb24) {
            this.rgb24 = rgb24;

            this.red = (rgb24 >> 16) & 0xFF;
            this.green = (rgb24 >> 8) & 0xFF;
            this.blue = rgb24 & 0xFF;

            this.red_float = ((float) red) / 255;
            this.green_float = ((float) green) / 255;
            this.blue_float = ((float) blue) / 255;
        }

    }

